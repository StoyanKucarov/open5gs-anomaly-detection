"""Run the full fault-atlas analysis suite over all 22 faults.

Usage:  cd reproduce && python -m analysis.run_all
Outputs land in reproduce/data/analysis/ (CSV/JSON + plots/).
"""
from __future__ import annotations

import warnings

import numpy as np

import analysis.common as c
from analysis import (a01_fault_atlas, a02_temporal, a03_propagation,
                       a04_discriminability, a05_k8s_blindspot,
                       a06_modality_necessity, a07_recovery, a08_taxonomy_map,
                       a09_nf_impact)

warnings.filterwarnings("ignore")
np.seterr(all="ignore")

README = """# Fault-atlas analysis suite — outputs

Generated by `analysis/run_all.py` from the 22-fault `reproduce/` dataset
(`data/experiments/C-fault-detection/<slug>/`, phases pre/during/post).

## Detection methodology (cite in paper)

- **t0 (injection)** = Chaos-Mesh `Applied` event time; fallback timeline.fault.start.
- **Continuous signals** (Prometheus gauges/rates, Beyla, Loki rates, Jaeger
  error-rate/p95, RTT): rolling z-score vs the fault's own `pre` baseline,
  window = {WINDOW_S}s ({WIN} x {BIN_S}s bins), crossing at |Δ| > max(Z·σ,
  {FLAT_REL:.0%}·|μ|) with **Z = {Z}** (flat-baseline guard), requiring
  **{PERSIST} consecutive** windows; first such window = time-to-detect.
- **Failure counters** (`*_fail`, `*_reject`, `gtp_node_failed`): first
  {COUNTER_PERSIST} consecutive non-zero samples above the pre-phase max.
- **pod restarts**: step over pre-phase level. **pod ready/running**: drop to 0.
- **K8s events**: presence of a Warning reason in the fault window.
- **NRF snapshot**: pre-vs-during registration delta (no intra-phase timing).

## Caveat metrics — collected but EXCLUDED from scoring (EXTENSIONS.md §10.10)

{CAVEATS}

`open5gs_upf_session_nbr` / `upf_qos_flows` / `smf_session_nbr` over-count;
`pfcp_sessions_active` / `smf_ues_active` are scored instead. GTP-N3 packet
counters and `rm_regtime` are not exported by Open5GS 2.7.5, so control-plane
latency is observable only via RTT/traces; the re-registration workload is
light (3/10 UEs) so AMF/registration faults are under-stimulated.

## Files

| File | Analysis |
|---|---|
| fault_atlas.csv / _by_layer.csv / _summary.txt | 01 binary fault×signal matrix, layer-grouped |
| temporal_layers.csv / first_signal_per_fault.csv | 02 time-to-first-signal per layer, layer ordering |
| propagation_summary.csv / propagation/<slug>.json | 03 cross-NF blast radius & onset chain |
| nf_fault_matrix.csv / nf_fault_onset.csv / nf_impact_summary.csv | 09 NF×fault impact matrix (which NF breaks under which fault) |
| discriminability.csv / signal_correlation.csv / fingerprints.csv | 04 signal specificity & fault fingerprints |
| k8s_blindspot.csv | 05 faults invisible to Kubernetes (Flora 2022) |
| modality_necessity.csv | 06 single-modality sufficiency (Fu 2025) |
| recovery.csv | 07 post-fault return-to-baseline |
| fault_taxonomy.csv | 08 master atlas: Zhou×Silva×RQ5 join |
| sensitivity.csv | 10 threshold-robustness sweep — run separately: `python -m analysis.a10_sensitivity` (~2 min) |
| plots/ | heatmaps & bar charts for all of the above |
"""


def main() -> None:
    c.ensure_dirs()
    print(f"Faults: {len(c.fault_slugs())}")
    a01_fault_atlas.run()
    a02_temporal.run()
    a03_propagation.run()
    a09_nf_impact.run()
    a04_discriminability.run()
    a05_k8s_blindspot.run()
    a06_modality_necessity.run()
    a07_recovery.run()
    a08_taxonomy_map.run()
    # a10 (threshold sensitivity) recomputes detection 7x (~2 min) — it is a
    # one-off robustness check for the paper, not part of the fast default.
    # Run it on demand: python -m analysis.a10_sensitivity
    (c.OUT_DIR / "README.md").write_text(README.format(
        WINDOW_S=c.WINDOW_S, WIN=c.WIN, BIN_S=c.BIN_S, Z=c.Z,
        FLAT_REL=c.FLAT_REL, PERSIST=c.PERSIST, COUNTER_PERSIST=c.COUNTER_PERSIST,
        CAVEATS="\n".join(f"- `{s.source}` ({s.key})" for s in c.SIGNALS if s.caveat)))
    print(f"\nDone. Outputs in {c.OUT_DIR}")


if __name__ == "__main__":
    main()
